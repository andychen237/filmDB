<!-- Returns top 5 highest-grossing films of a given decade -->
<?php

//Open a connection to dbase server
include 'open.php';

//Construct an array in which we'll store our data
$dataPoints = array();

//Prepare a statement to be executed later,
if ($stmt = $conn->prepare("CALL TopFiveDecade(?)")) {

    //Bind variables to the missing data value denoted by ? above
    $stmt->bind_param("s", $Year);
        
    //Set the value of the bound variables
    $Year = $_POST['Year'];
    
    //Execute the statement on dbase
    if (!empty($Year)) {
   	    if ($stmt->execute()) {

      		//Store result set generated by the prepared statement
        	$result = $stmt->get_result();

			if ($result->num_rows == 0) {
	   			echo "no revenue information for this decade";
			} else {
				foreach($result as $row) {
					array_push($dataPoints, array("label"=> $row["title"],
		   			"y"=> $row["revenue_adj"]));
	   			}
			}
		//Free the result set returned from the query, since we've stored data in array
        $result->free_result();
 		} else {
			echo "Please make sure your input is valid.";
		}
    } else {
	    echo "Please input a year.";
    }
}

//Close the prepared statement
$stmt->close();

//Close the connection opened by open.php since we no longer need access to dbase
$conn->close();
?>	   
    
